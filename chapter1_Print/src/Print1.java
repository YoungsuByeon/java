
public class Print1 {
	public static void main (String [] args) {
		
		System.out.println("자바에 오신것을 환영합니다");     // 개행 - 줄바꿈
		System.out.printf("%s에 오신것을 %s합니다\n","자바","환영");    // 개행(x) - 줄바꿈안됨, 출력폼을 정해서 줄력가능
		                          //          %c,   %s,  %f,  %d
		System.out.print("자바에 오신것을 환영합니다");       // 개행(x)

	}

}
